def adicao(numeros):
    return sum(numeros)

def subtracao(numeros):
    resultado = numeros[0]
    for n in numeros[1:]:
        resultado -= n
    return resultado

def multiplicacao(numeros):
    resultado = 1
    for n in numeros:
        resultado *= n
    return resultado

